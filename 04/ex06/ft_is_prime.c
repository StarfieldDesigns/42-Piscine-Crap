int		ft_is_prime(int nb)
{
	int a;

	if (nb < 1)
	{
		return (0);
	}
	if (nb == 2 || nb == 3)
	{
		return (1);
	}
	if (nb % 2 == 0 || nb % 3 == 0)
	{
		return (0);
	}
	a = 3;
	while (a * a <= nb)
	{
		if (nb % a == 0)
		{
			return (0);
		}
		a = a + 2;
	}	
	return (1);
}

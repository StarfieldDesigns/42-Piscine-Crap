char	*ft_strupcase(char *str)
{
	char	tmp[100];
	int		a;

	a = 0;
	while (str[a] != '\0')
	{
		if ((str[a] >= 'A' && *str <= 'Z') || (*str >= '0' && *str <= '9'))
		{
			tmp[a] = (str[a]);
			a++;
		}
		if (str[a] >= 'a' && *str <= 'z')
		{
			tmp[a] = (str[a] - 32);
			a++;
		}
		else
		{
			tmp[a] = str[a];
			a++;
		}
	}
	str = tmp;
	tmp[a] = '\0';
	return (str);
}
